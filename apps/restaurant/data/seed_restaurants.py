def normalize_name_key(name: str) -> str:
    s = name.strip().lower()
    s = re.sub(r"\s+", "", s)
    for suffix in ("강남점", "홍대점", "명동점", "본점", "역삼점", "신촌점", "잠실점"):
        s = s.replace(suffix.lower(), "")
    return s


